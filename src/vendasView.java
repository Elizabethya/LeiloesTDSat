    private void listarVendidos() {
    try {
        ProdutosDAO produtosdao = new ProdutosDAO();
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tabelaVendidos.getModel();
        model.setNumRows(0); // limpa a tabela

        java.util.ArrayList<ProdutosDTO> listagem = produtosdao.listarProdutosVendidos();

        for (int i = 0; i < listagem.size(); i++) {
            model.addRow(new Object[]{
                listagem.get(i).getId(),
                listagem.get(i).getNome(),
                listagem.get(i).getValor(),
                listagem.get(i).getStatus()
            });
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
}
